public class while1 {
    
    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        // Programa em Java que mostra os números ímpares
        System.out.println("números ímpares: ");
        int num = 1;
        while (num <= 100) {
            System.out.print(num + " ");
            num += 2;
        }
    }
    
}
